        let currentSheet = null;

        document.addEventListener('DOMContentLoaded', () => {
            renderTabs();
            // Automatically select 'Link' tab or the first one if not present
            const preferredTabs = ['Link', 'PBI', 'Workgenda'];
            currentSheet = Object.keys(progressData)[0];
            for (let i = 0; i < preferredTabs.length; i++) {
                if (progressData[preferredTabs[i]]) {
                    currentSheet = preferredTabs[i];
                    break;
                }
            }
            if (currentSheet) renderSheet(currentSheet);
        });

        function renderTabs() {
            const tabsContainer = document.getElementById('sheetTabs');
            tabsContainer.innerHTML = '';

            const targetTabs = ['Link', 'PBI', 'Workgenda'];
            const sheetNames = Object.keys(progressData);
            const sheetsToShow = sheetNames.filter(name => targetTabs.includes(name));
            const finalSheets = sheetsToShow.length > 0 ? sheetsToShow : sheetNames;

            finalSheets.forEach(sheetName => {
                const btn = document.createElement('button');
                btn.className = 'tab-btn';
                btn.textContent = sheetName;
                btn.onclick = () => renderSheet(sheetName);
                tabsContainer.appendChild(btn);
            });
        }

        function renderSheet(sheetName) {
            currentSheet = sheetName;

            document.querySelectorAll('.tab-btn').forEach(btn => {
                if (btn.textContent === sheetName) {
                    btn.classList.add('active');
                } else {
                    btn.classList.remove('active');
                }
            });

            const rows = progressData[sheetName];
            if (!rows || rows.length === 0) return;

            const thead = document.getElementById('tableHead');
            const tbody = document.getElementById('tableBody');

            thead.innerHTML = '';
            tbody.innerHTML = '';

            // Render header (first row)
            const headerRowData = rows[0];
            const theadTr = document.createElement('tr');
            headerRowData.forEach(cellVal => {
                const th = document.createElement('th');
                th.textContent = cellVal;
                theadTr.appendChild(th);
            });
            thead.appendChild(theadTr);

            // Render rows (row 2 onwards)
            for (let r = 1; r < rows.length; r++) {
                const rowData = rows[r];
                const tr = document.createElement('tr');
                rowData.forEach(cellVal => {
                    const td = document.createElement('td');
                    // Check if link
                    if (typeof cellVal === 'string' && cellVal.startsWith('http')) {
                        const a = document.createElement('a');
                        a.href = cellVal;
                        a.target = '_blank';
                        a.textContent = 'Link ↗';
                        a.style.color = 'var(--accent-primary)';
                        td.appendChild(a);
                    } else {
                        td.textContent = cellVal;
                    }
                    tr.appendChild(td);
                });
                tbody.appendChild(tr);
            }
        }

        // Listen for download requests from the parent window
        window.addEventListener('message', function(event) {
            if (event.data === 'download_excel') {
                if (typeof XLSX === 'undefined') {
                    alert("Excel export library (xlsx.full.min.js) is missing. Make sure it is in the same folder.");
                    return;
                }
                
                // Create a new workbook
                const wb = XLSX.utils.book_new();
                
                // Generate a sheet for every section in progressData
                Object.keys(progressData).forEach(sheetName => {
                    const rows = progressData[sheetName];
                    const ws = XLSX.utils.aoa_to_sheet(rows);
                    XLSX.utils.book_append_sheet(wb, ws, sheetName);
                });
                
                // Trigger download of the reconstructed file
                XLSX.writeFile(wb, "Progress_Report.xlsx");
            }
        });
